# Tipe data numerik
angka_bulat = 10
angka_desimal = 3.14

# Tipe data string
teks = "Halo, ini adalah contoh string"

# Tipe data boolean
benar = True
salah = False

# Tipe data list
daftar_angka = [1, 2, 3, 4, 5]
daftar_teks = ["apel", "jeruk", "pisang"]

# Tipe data tuple
tuple_angka = (10, 20, 30)
tuple_teks = ("senin", "selasa", "rabu")

# Tipe data set
set_angka = {1, 2, 3, 4, 5}
set_teks = {"apel", "jeruk", "pisang"}

# Tipe data dictionary
dict_mahasiswa = {"nama": "Fauzi", "usia": 20, "jurusan": "Ilmu Komputer"}

# Menampilkan nilai dari masing-masing tipe data
print("Angka bulat:", angka_bulat)
print("Angka desimal:", angka_desimal)
print("Teks:", teks)
print("Boolean (Benar):", benar)
print("Boolean (Salah):", salah)
print("List angka:", daftar_angka)
print("List teks:", daftar_teks)
print("Tuple angka:", tuple_angka)
print("Tuple teks:", tuple_teks)
print("Set angka:", set_angka)
print("Set teks:", set_teks)
print("Dictionary mahasiswa:", dict_mahasiswa)
